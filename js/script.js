// La función scrollToTop() utiliza el método window.scrollTo() para desplazarse hacia la parte superior de la página.
// La opción top: 0 especifica que queremos desplazarnos hasta la parte superior de la página. 
// La opción behavior: 'smooth' hace que el desplazamiento sea suave y animado.
function scrollToTop() {

    window.scrollTo({
  
      top: 0,
  
      behavior: 'smooth'
  
    });
  
  }